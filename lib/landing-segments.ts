export type SegmentId = "marketing" | "it" | "manager";

export interface SegmentData {
  id: SegmentId;
  /** Path without leading slash, e.g. "rezume-marketologu". */
  slug: string;
  meta: {
    title: string;
    description: string;
    ogTitle: string;
    ogDescription: string;
  };
  hero: {
    pill: string;
    /** Two lines, rendered with a <br/> between them. */
    h1: [string, string];
    lead: string;
  };
  pain: {
    /** Two lines for the heading. */
    head: [string, string];
    rows: string[];
  };
  value: {
    head: { before: string; highlight: string; after: string };
    sub: string;
    cards: { title: string; items: string[] }[];
  };
  demo: {
    placeholder: string;
  };
  beforeAfter: {
    bad: string;
    good: string;
  };
  steps: { title: string; text: string }[];
  compare: {
    we: string[];
    them: string[];
  };
  emotions: string[];
  faq: { q: string; a: string }[];
  testimonials: {
    heading: string;
    /** Placeholders — replace with real reviews later. */
    quotes: string[];
  };
  /** Hardcoded examples used when the LLM fails/times out, per segment. */
  fallbacks: string[];
}

